int getIndex(jchar c) {
    if (c >= '0' && c <= '9')
        return c - '0';
    else if (c == '-')
        return 10;
    else if (c == ' ')
        return 11;
    else if (c == ':')
        return 12;
    else if(c==24180)//年
        return 13;
    else if(c==26376)//'月'
        return 14;
    else if(c==26085)//'日'
        return 15;
    return 11;
}

